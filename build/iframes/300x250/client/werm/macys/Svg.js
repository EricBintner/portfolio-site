
var Svg = Class.extend({
	///////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////
	//private var
	///////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////
	//---------------------------------------------------------
	//Boolean
	//---------------------------------------------------------
	comb_complete:false,
	//---------------------------------------------------------
	//String
	//---------------------------------------------------------
	output:undefined,
	id: "shell",
	//---------------------------------------------------------
	//Object
	//---------------------------------------------------------
	obj:undefined,
	//---------------------------------------------------------
	//Number
	//---------------------------------------------------------
	width:undefined,
	height:undefined,
	x:undefined,
	y:undefined,
	scale:undefined,
	//---------------------------------------------------------
	//$
	//---------------------------------------------------------
	$elm:undefined,
	$parent:undefined,
	$div:undefined,
	///////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////
	//methods
	///////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////////////////////////////////////////////////////
	init:function($elm,obj) {
		if ($elm != undefined) {
			this.obj = obj;
			if ($elm.length == 0) ThrowError.go("$elm does not exist, please make sure element exist in DOM.", false, this);
			//---------------------------------------------------
			this.$elm = $elm;
			this.$elm.addClass("svg");
			//---------------------------------------------------
			SetPublicProp.go(this,obj);
			//---------------------------------------------------
			this.$parent = this.$elm.closest(".content");
			//---------------------------------------------------
			this.origDisplay = this.$parent.css("display");
			//---------------------------------------------------
			this.$parent.css("display", "block");
			//---------------------------------------------------
			if (this.width == undefined) this.width = this.$parent.width();
			if (this.height == undefined) this.height = this.$parent.height();
			//---------------------------------------------------
			this.$parent.css("display", this.origDisplay);
			//---------------------------------------------------
			this.pre_output_CFG();
			//---------------------------------------------------
			this.output_CFG();	
			//---------------------------------------------------	
			this.$elm.html(this.output);
			//---------------------------------------------------	
			this.$div = this.$elm.find("svg > g");
			//---------------------------------------------------
			/*if (this.x == undefined && this.$div[0]._gsTransform == undefined) this.x = 0;
			if (this.y == undefined && this.$div[0]._gsTransform == undefined) this.y = 0;
			if (this.scale == undefined && this.$div[0]._gsTransform == undefined) this.scale = 1;
			TweenLite.set(this.$div, {rotation:.5});*/
			//---------------------------------------------------
			this.start();
		}
	},
	//------------------------------------------------------------------------------------- 
	comb:function() {
		if (!window[this.classID].comb_complete) {
			window[this.classID].comb_complete = true;
			for (var i = 0; i < $("."+this.classID).length; i++) {
				window[this.classID].go($("."+this.classID).eq(i));
			}
		}
	},
	//------------------------------------------------------------------------------------- 
	start:function() {
	},
	//------------------------------------------------------------------------------------- 
	pre_output_CFG:function() {
	},
	//------------------------------------------------------------------------------------- 
	output_CFG:function() {
	}
});
