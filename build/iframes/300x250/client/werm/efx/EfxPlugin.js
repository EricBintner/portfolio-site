
var EfxPlugin_cls = Class.extend({
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//init var
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//------------------------------------------------------------------------
	//Array
	//------------------------------------------------------------------------
	pluginArr:[],
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//Constructor
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	init:function() {
	},
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//General Methods
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	getPlugin:function(_string, error) {
		if (error == undefined) error = true;
		//------------------------------------------------------
		//init var
		//------------------------------------------------------
		var classReference;
		var i;
		//------------------------------------------------------
		for (i = 0; i < pluginArr.length; i++) {
			if (getQualifiedClassName(pluginArr[i]).indexOf(_string) > 0) {
				classReference = pluginArr[i];
			}
		}
		//------------------------------------------------------
		if (classReference == null && error) {
			ThrowError.go("Plugin '"+_string+"' not activated!!!!!!!!");
		}
		return classReference;
	},
	//------------------------------------------------------------------------------------------------------------------
	activate:function(arr) {
		this.pluginArr = this.pluginArr.concat(arr);
	}
});
var EfxPlugin = new EfxPlugin_cls();
